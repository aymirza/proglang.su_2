package _8_Operatori_priniatia_resheniy;

public class Operatori_if_else_2 {
    public static void main(String[] args) {
        int x = 20;
        if (x == 10) {
            System.out.println("Значение X = 10");
        } else if (x == 20) {
            System.out.println("Значение X = 20");
        } else if (x==30) {
            System.out.println("Значение X = 30");
        }else {
            System.out.println("Оператор else");
        }
    }
}
